# 비트연산자

# ( 비트 단위 )
# &     : and
# |     : or
# ^     : xor => (a = 1 / b = 0) -> a^b == 1
# ~     : not
# <<    : 왼쪽 시프트
# >>    : 오른쪽 시프트

# 정수를 2진수로 변환 후
# 13 & 6  => 1101 & 0110 = 0100 => 4
# 13 | 6  => 1101 | 0110 = 1111 => 15
# 13 ^ 6  => 1101 ^ 0110 = 1011 => 11
# 4 >> 1  => 0100   >> 1 = 0010 => 2
# 4 << 1  => 0100   << 1 = 1000 => 8
# 16 >> 4 => 1 0000 >> 4 = 0001 => 1

# and -> 논리곱
# or -> 논리합
